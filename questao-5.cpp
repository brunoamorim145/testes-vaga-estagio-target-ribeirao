/*Questão 5) Você está em uma sala com três interruptores, cada um conectado a uma 
lâmpada em salas diferentes. Você não pode ver as lâmpadas da sala em que está, mas 
pode ligar e desligar os interruptores quantas vezes quiser. Seu objetivo é descobrir 
qual interruptor controla qual lâmpada. Como você faria para descobrir, usando apenas 
duas idas até uma das salas das lâmpadas, qual interruptor controla cada lâmpada? 

Resposta: 
 Ligo um interruptor e deixo ligado alguns minutos depois verifico a temperatura das lâmpadas,
a que está mais quente corresponde ao interruptor que foi ligado. Ligo outro interruptor e deixo
ligado por alguns minutos e verifico as lâmpadas novamente. Por fim só irá sobrar um interruptor 
que não foi ligado, correspondendo a lâmpada restante.*/
